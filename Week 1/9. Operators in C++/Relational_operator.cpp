#include<iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 5;
    int c=5;

    //RELATIONAL OPERATORS IN C++
    cout<<"Is 'a' greater than 'b'? "<<(a>b)<<endl;
    cout<<(a<b)<<endl;  //0
    cout<<(a>=b)<<endl; //1 
    cout<<(a<=b)<<endl; //0
    cout<<(a==b)<<endl; //0
    cout<<(a!=b)<<endl; //1
    cout<<(b==c)<<endl; //1

}